import countriesData from '../../public/data/countries.json';

class Country {
    constructor(id:string, name_ch:string, name:string, area:number) {
        this.id = id

        this.name_ch = name_ch
        this.name = name
        this.area = area
    }  
    id: string;
    name_ch: string;
    name: string;
    area: number;
}

class Province {
    constructor(id:string, countryId:string, name_ch:string, name:string, area:number) {
        this.id = id
        this.countryId = countryId
        this.name_ch = name_ch
        this.name = name
        this.area = area
    }  
    id: string;
    countryId: string;
    name_ch: string;
    name: string;
    area: number;
}

export const getCountries = (): Array<Country> => {
    console.log("this is getCountries()");
    return countriesData.map((item, idx) => new Country(
        String(item.id),
        item.name_ch,
        item.name,
        item.area
    ));
};

